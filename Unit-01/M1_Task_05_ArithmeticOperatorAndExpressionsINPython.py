# Arithmetic Operator
'''
  Addition[+]
  Substraction[-]
  Multiplication[*]
  Division      [/]
  Modulus       [%]
  Exponentiation [**]
  Floor Division [//]
'''
val1=2
val2=3
res=val1+val2
print(res)

res=val1-val2
print(res)


res=val1*val2
print(res)


res=val1/val2
print(res)

res=val1%val2
print(res)

res=val1**val2
print(res)

res=val1//val2
print(res)


